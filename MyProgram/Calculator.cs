using System;
using System.Collections.Generic;
using System.Text;

namespace MyProgram
{
    public class Calculator
    {
        public int Sum(int a , int b)
        {
            return a + b;
        }

        public int Divide(int a, int b)
        {
            return a / b;
        }

        public int Multiplication(int a, int b)
        {
            return a * b;
        }

        public int Subtraction(int a, int b)
        {
            return a - b;
        }
    }
}
